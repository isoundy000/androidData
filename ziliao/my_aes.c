/**
* GF(2^8) 
**/
unsigned char FFmul(unsigned char a, unsigned char data){
	unsigned char temp[4];
	unsigned char i = 0;
	unsigned char ret = 0;
	temp[0] = data;
	for(i = 1; i < 4; i++){
		temp[i] = temp[i-1] << 1;			//left move
		if(temp[i-1] & 0x80){				//before left move, the high bit is 1
			temp[i] = temp[i] ^ 0x1b;
		}
	}
	//this place i don't understand
	for(i = 0 ; i < 4; i++){
		if((a >> i) & 0x01){
			ret = ret ^ temp[i];
		}
	}
	return ret;
}

