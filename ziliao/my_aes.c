/**
* S box
* replace the promary data
**/
void replace(unsigned char data[][4]){
	int i = 0;
	int j = 0;
	for(i = 0; i < 4 ; i++){
		for(j = 0; j < 4; j++){
			data[i][j] = sBox[data[i][j]];
		}
	}
}

/**
* move row
* 0 row -- left move 0  
* 1 row -- left move 1
* 2 row -- left move 2
* 3 row -- left move 3
**/
void shiftRow(unsigned char data[][4]){
	int i = 0;
	int j = 0;
	unsigned char temp[4];

	for(i = 1 ; i < 4; i++){
		for(j = 0; j < 4; j++){
			temp[j] = data[i][(i+j) % 4];
		}

		for(j = 0; j < 4 ; j++){
			data[i][j] = temp[j];
		}
	}
}


